def main():
    parser = argparse.ArgumentParser(description="生成冷链 LoRA SFT 数据集")
    parser.add_argument("--count", type=int, default=300, help="目标条数（不足则用现有全部）")
    parser.add_argument("--with-eval", action="store_true", help="同时扩充 test_cases.json")
    parser.add_argument("--eval-count", type=int, default=40, help="评估集条数")
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(message)s")

    all_records = _build_all_records()
    if len(all_records) < args.count:
        logger.warning("生成 %d 条，少于目标 %d，将全部写出", len(all_records), args.count)
        selected = all_records
    else:
        selected = all_records[: args.count]

    QA_OUT.parent.mkdir(parents=True, exist_ok=True)
    with open(QA_OUT, "w", encoding="utf-8") as f:
        json.dump(selected, f, ensure_ascii=False, indent=2)

    # 统计
    temp_n = sum(1 for r in selected if "°C" in r["answer"] or "℃" in r["answer"])
    reg_n = sum(
        1
        for r in selected
        if any(k in r["answer"] for k in ("GSP", "GB/T", "规范", "标准", "药典"))
    )
    fmt_n = sum(1 for r in selected if "【结论】" in r["answer"] and "【依据】" in r["answer"])

    print(f"已写入 {len(selected)} 条 → {QA_OUT}")
    print(f"  含温度数值: {temp_n} ({temp_n/len(selected):.0%})")
    print(f"  含法规引用: {reg_n} ({reg_n/len(selected):.0%})")
    print(f"  三段式格式: {fmt_n} ({fmt_n/len(selected):.0%})")

    if args.with_eval:
        cases = _build_eval_cases(selected, n=args.eval_count)
        with open(EVAL_OUT, "w", encoding="utf-8") as f:
            json.dump(cases, f, ensure_ascii=False, indent=2)
        print(f"评估集 {len(cases)} 条 → {EVAL_OUT}")


if __name__ == "__main__":
    main()
